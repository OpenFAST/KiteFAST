 SUBROUTINE KFC_PackOutput( ReKiBuf, DbKiBuf, IntKiBuf, Indata, ErrStat, ErrMsg, SizeOnly )
  REAL(ReKi),       ALLOCATABLE, INTENT(  OUT) :: ReKiBuf(:)
  REAL(DbKi),       ALLOCATABLE, INTENT(  OUT) :: DbKiBuf(:)
  INTEGER(IntKi),   ALLOCATABLE, INTENT(  OUT) :: IntKiBuf(:)
  TYPE(KFC_OutputType),  INTENT(IN) :: InData
  INTEGER(IntKi),   INTENT(  OUT) :: ErrStat
  CHARACTER(*),     INTENT(  OUT) :: ErrMsg
  LOGICAL,OPTIONAL, INTENT(IN   ) :: SizeOnly
    ! Local variables
  INTEGER(IntKi)                 :: Re_BufSz
  INTEGER(IntKi)                 :: Re_Xferred
  INTEGER(IntKi)                 :: Db_BufSz
  INTEGER(IntKi)                 :: Db_Xferred
  INTEGER(IntKi)                 :: Int_BufSz
  INTEGER(IntKi)                 :: Int_Xferred
  INTEGER(IntKi)                 :: i,i1,i2,i3,i4,i5
  LOGICAL                        :: OnlySize ! if present and true, do not pack, just allocate buffers
  INTEGER(IntKi)                 :: ErrStat2
  CHARACTER(ErrMsgLen)           :: ErrMsg2
  CHARACTER(*), PARAMETER        :: RoutineName = 'KFC_PackOutput'
 ! buffers to store subtypes, if any
  REAL(ReKi),      ALLOCATABLE   :: Re_Buf(:)
  REAL(DbKi),      ALLOCATABLE   :: Db_Buf(:)
  INTEGER(IntKi),  ALLOCATABLE   :: Int_Buf(:)

  OnlySize = .FALSE.
  IF ( PRESENT(SizeOnly) ) THEN
    OnlySize = SizeOnly
  ENDIF
    !
  ErrStat = ErrID_None
  ErrMsg  = ""
  Re_BufSz  = 0
  Db_BufSz  = 0
  Int_BufSz  = 0
  Int_BufSz   = Int_BufSz   + 1     ! SFlp allocated yes/no
  IF ( ALLOCATED(InData%SFlp) ) THEN
    Int_BufSz   = Int_BufSz   + 2*1  ! SFlp upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%SFlp)  ! SFlp
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! PFlp allocated yes/no
  IF ( ALLOCATED(InData%PFlp) ) THEN
    Int_BufSz   = Int_BufSz   + 2*1  ! PFlp upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%PFlp)  ! PFlp
  END IF
      Re_BufSz   = Re_BufSz   + SIZE(InData%Rudr)  ! Rudr
      Re_BufSz   = Re_BufSz   + SIZE(InData%SElv)  ! SElv
      Re_BufSz   = Re_BufSz   + SIZE(InData%PElv)  ! PElv
  Int_BufSz   = Int_BufSz   + 1     ! SPyRtrSpd allocated yes/no
  IF ( ALLOCATED(InData%SPyRtrSpd) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! SPyRtrSpd upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%SPyRtrSpd)  ! SPyRtrSpd
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! PPyRtrSpd allocated yes/no
  IF ( ALLOCATED(InData%PPyRtrSpd) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! PPyRtrSpd upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%PPyRtrSpd)  ! PPyRtrSpd
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! SPyRtrAcc allocated yes/no
  IF ( ALLOCATED(InData%SPyRtrAcc) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! SPyRtrAcc upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%SPyRtrAcc)  ! SPyRtrAcc
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! PPyRtrAcc allocated yes/no
  IF ( ALLOCATED(InData%PPyRtrAcc) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! PPyRtrAcc upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%PPyRtrAcc)  ! PPyRtrAcc
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! SPyGenTorque allocated yes/no
  IF ( ALLOCATED(InData%SPyGenTorque) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! SPyGenTorque upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%SPyGenTorque)  ! SPyGenTorque
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! PPyGenTorque allocated yes/no
  IF ( ALLOCATED(InData%PPyGenTorque) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! PPyGenTorque upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%PPyGenTorque)  ! PPyGenTorque
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! SPyBldPitch allocated yes/no
  IF ( ALLOCATED(InData%SPyBldPitch) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! SPyBldPitch upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%SPyBldPitch)  ! SPyBldPitch
  END IF
  Int_BufSz   = Int_BufSz   + 1     ! PPyBldPitch allocated yes/no
  IF ( ALLOCATED(InData%PPyBldPitch) ) THEN
    Int_BufSz   = Int_BufSz   + 2*2  ! PPyBldPitch upper/lower bounds for each dimension
      Re_BufSz   = Re_BufSz   + SIZE(InData%PPyBldPitch)  ! PPyBldPitch
  END IF
  IF ( Re_BufSz  .GT. 0 ) THEN 
     ALLOCATE( ReKiBuf(  Re_BufSz  ), STAT=ErrStat2 )
     IF (ErrStat2 /= 0) THEN 
       CALL SetErrStat(ErrID_Fatal, 'Error allocating ReKiBuf.', ErrStat, ErrMsg,RoutineName)
       RETURN
     END IF
  END IF
  IF ( Db_BufSz  .GT. 0 ) THEN 
     ALLOCATE( DbKiBuf(  Db_BufSz  ), STAT=ErrStat2 )
     IF (ErrStat2 /= 0) THEN 
       CALL SetErrStat(ErrID_Fatal, 'Error allocating DbKiBuf.', ErrStat, ErrMsg,RoutineName)
       RETURN
     END IF
  END IF
  IF ( Int_BufSz  .GT. 0 ) THEN 
     ALLOCATE( IntKiBuf(  Int_BufSz  ), STAT=ErrStat2 )
     IF (ErrStat2 /= 0) THEN 
       CALL SetErrStat(ErrID_Fatal, 'Error allocating IntKiBuf.', ErrStat, ErrMsg,RoutineName)
       RETURN
     END IF
  END IF
  IF(OnlySize) RETURN ! return early if only trying to allocate buffers (not pack them)

  Re_Xferred  = 1
  Db_Xferred  = 1
  Int_Xferred = 1

  IF ( .NOT. ALLOCATED(InData%SFlp) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%SFlp,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%SFlp,1)
    Int_Xferred = Int_Xferred + 2

      IF (SIZE(InData%SFlp)>0) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%SFlp))-1 ) = PACK(InData%SFlp,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%SFlp)
  END IF
  IF ( .NOT. ALLOCATED(InData%PFlp) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%PFlp,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%PFlp,1)
    Int_Xferred = Int_Xferred + 2

      IF (SIZE(InData%PFlp)>0) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%PFlp))-1 ) = PACK(InData%PFlp,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%PFlp)
  END IF
      ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%Rudr))-1 ) = PACK(InData%Rudr,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%Rudr)
      ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%SElv))-1 ) = PACK(InData%SElv,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%SElv)
      ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%PElv))-1 ) = PACK(InData%PElv,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%PElv)
  IF ( .NOT. ALLOCATED(InData%SPyRtrSpd) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%SPyRtrSpd,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%SPyRtrSpd,1)
    Int_Xferred = Int_Xferred + 2
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%SPyRtrSpd,2)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%SPyRtrSpd,2)
    Int_Xferred = Int_Xferred + 2

      IF (SIZE(InData%SPyRtrSpd)>0) ReKiBuf ( Re_Xferred:Re_Xferred+(SIZE(InData%SPyRtrSpd))-1 ) = PACK(InData%SPyRtrSpd,.TRUE.)
      Re_Xferred   = Re_Xferred   + SIZE(InData%SPyRtrSpd)
  END IF
  IF ( .NOT. ALLOCATED(InData%PPyRtrSpd) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%PPyRtrSpd,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%PPyRtrSpd,1)
